public class ClasseA {
    int valorPrimari;
    int valorSecundari;

    public ClasseA() {
        valorPrimari = 5;
        valorSecundari = 10;
    }

    public ClasseA(int vp) {
        valorPrimari = vp;
        valorSecundari = 10;
    }

    public ClasseA(int vp, int vs) {
        valorPrimari = vp;
        valorSecundari = vs;
    }
}
